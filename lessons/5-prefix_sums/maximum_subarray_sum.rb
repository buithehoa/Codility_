=begin
  Problem: Maximum Subarray Sum with at Most K Negative Elements
  Given an array of integers and an integer K, find the maximum sum of a subarray that contains at most K negative numbers.

  Example:

  Input: 
  Array: [1, -2, 3, -1, 4, -2, 5]
  K: 1

  Output: 7

  Explanation:
    The subarray [3, -1, 4, -2, 5] has maximum sum 9 but contains 2 negative numbers.
    The subarray [3, -1, 4] has sum 6 with 1 negative number.
    The subarray [4, -2, 5] has sum 7 with 1 negative number.
    So the maximum sum with at most 1 negative number is 7.
=end

